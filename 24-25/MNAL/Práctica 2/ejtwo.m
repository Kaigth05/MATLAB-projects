function [L,U] = ejtwo
I=eye(3);
A=[-3, 1, -1;
    9, -2, 3;
    -6, 1, 5];
e1=I(:,1);
e2=I(:,2);
e3=I(:,3);
A1 = A - (A(2,1)/A(1,1))*e2*A(1,:) - (A(3,1)/A(1,1))*e3*A(1,:);
A2 = A1 - (A1(3,2)/A1(2,2))*e3*A1(2,:);
U = A2;
L = I + (A(2,1)/A(1,1))*e2*e1' + (A(3,1)/A(1,1))*e3*e1' + ...
(A1(3,2)/A1(2,2))*e3*e2';
end
